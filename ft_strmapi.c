#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	size_t	l;
	char	*new;

	i = 0;
	l = ft_strlen(s);
	if (!(new = (char *)malloc(sizeof(char) * (l + 1))))
		return (0);
	while (s[i] != '\0')
	{
		new[i] = f(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
